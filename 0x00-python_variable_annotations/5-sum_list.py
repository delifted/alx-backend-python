#!/usr/bin/env python3
'''5-sum_list.py'''

from typing import List


def sum_list(input_list: List[float]) -> float:
    '''5-sum_list.py
    Compute the sum of a list of floats.

    Args:
        input_list (List[float]): List of floats.

    Returns:
        float: Sum of the elements in the input list
        '''
    return sum(input_list)
